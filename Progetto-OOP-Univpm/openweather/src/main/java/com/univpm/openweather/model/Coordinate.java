package com.univpm.openweather.model;

public class Coordinate {
	
	private int lon;
	private int lat;
	
	//getter
	public int getLon() {
		return lon;
	}
	public int getLat() {
		return lat;
	}
	//setter
	public void setLon(int lon) {
		this.lon = lon;
	}
	public void setLat(int lat) {
		this.lat = lat;
	}
	

}
